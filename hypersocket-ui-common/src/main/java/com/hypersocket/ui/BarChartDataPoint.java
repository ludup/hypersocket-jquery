package com.hypersocket.ui;

public class BarChartDataPoint {

	String x;
	long y;
	
	public BarChartDataPoint(String x, long y) {
	
		this.x = x;
		this.y = y;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public long getY() {
		return y;
	}

	public void setY(long y) {
		this.y = y;
	}
	
	

	
}

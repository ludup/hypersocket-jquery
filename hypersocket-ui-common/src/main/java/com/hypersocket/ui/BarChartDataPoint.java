package com.hypersocket.ui;

public class BarChartDataPoint {

	private String x;
	private Long y;
	
	public BarChartDataPoint(String x, Long y) {
	
		this.x = x;
		this.y = y;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public Long getY() {
		return y;
	}

	public void setY(Long y) {
		this.y = y;
	}
	
	

	
}
